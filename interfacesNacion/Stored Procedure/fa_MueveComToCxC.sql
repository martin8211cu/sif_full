create proc fa_MueveComToCxC
  @FAM01COD  char(4)
 ,@FAX01NTR  int
as 
/* 
** fa_MueveComToCxC
** Creado por:  soin en 
** Fecha:       08-Jun-2007
*/
declare @Total money, @PagadoNC money 
 insert CCTN015 
            (CCM21COD, FACDOC,  CLICOD, FAM01COD, FAX01NTR, FAX01FEC, FAX01TOT, 
               FAX42C16, FAX42C5, STATUS, MONCOD, FAX01FCAM, CLINOM)
      select distinct FAX001EX.CCM21COD, FAX011.FACDOC, FAX001.CLICOD, @FAM01COD, @FAX01NTR, FAX001.FAX01FEC, FAX01TOT, 
               case when FAX42C16D >0 then 0 else FAX42C16 end
            , case when FAX42C5D > 0  then 0 else FAX42C5 end,
             'N', FAX001.MONCOD, FAX001.FAX01FCAM, FAX01NDOC
		FROM  FAX001, FAX042, FAX011, FAX001EX, FAX004
		WHERE FAX001.FAM01COD = @FAM01COD 
          and FAX001.FAX01NTR = @FAX01NTR
          and FAX001.FAM01COD = FAX004.FAM01COD
          and FAX001.FAX01NTR = FAX004.FAX01NTR
          and FAX001.FAM01COD = FAX042.FAM01COD
          and FAX001.FAX01NTR = FAX042.FAX01NTR
          and FAX001.FAM01COD = FAX011.FAM01COD
          and FAX001.FAX01NTR = FAX011.FAX01NTR
          and FAX001EX.CCM21COD != null and FAX001EX.CCM21COD != 'ND'
   		  and FAX011.FAX11LIN = (select max(FAX11LIN) from FAX011 b where b.FAM01COD = @FAM01COD and b.FAX01NTR = @FAX01NTR)
	  and FAX001EX.FAX01ENUMDOC = FAX004.FAX04NTE
	  and FAX001EX.FAX01ESIS = FAX004.FAX04CDE
          and ((case when FAX42C16D >0 then 0 else FAX42C16 end) > 0 or 
               ( case when FAX42C5D > 0  then 0 else FAX42C5 end) > 0 )
  insert CCTN015DPP 
                    (CCM21COD, FACDOC, CCTN15DPPLINEA,  FAM01COD, FAX01NTR, CCT15PPMTO, CCT15CTRO_COSTO, CCT15PROD, CTAM04, CTAM05, CTAM06)
            select  CCM21COD, FAX011.FACDOC, FAX42LINPP,  @FAM01COD, @FAX01NTR, FAX42PPMTO, FAX42CTRO_COSTO, FAX42PRODUCTO, CTAM04, CTAM05, CTAM06
              from FAX042DPP, FAX011
              where FAX042DPP.FAM01COD = @FAM01COD
                and FAX042DPP.FAX01NTR = @FAX01NTR
                and FAX042DPP.FAM01COD = FAX011.FAM01COD
                and FAX042DPP.FAX01NTR = FAX011.FAX01NTR
         		and FAX011.FAX11LIN = (select max(FAX11LIN) from FAX011 b where b.FAM01COD = @FAM01COD and b.FAX01NTR = @FAX01NTR)
 
  select @PagadoNC = coalesce(sum(FAX12TOTMF),0)
   from FAX012
   where FAM01COD = @FAM01COD
     and FAX01NTR = @FAX01NTR
     and FAX012.FAX12TIP = 'NC'
--  select @Total = sum(isnull(FAX04TOT, 0)  - ( isnull(FAX04IMP, 0)) )
  select @Total = sum(isnull(FAX04TOT, 0)  - ( isnull(FAX04IMP, 0)+ isnull(FAX04ETIMBRE, 0)))
    FROM FAX004, FAX004EX
    where FAX004.FAM01COD = @FAM01COD
      and FAX004.FAX01NTR = @FAX01NTR
	  and FAX004EX.FAX01ENUMDOC = FAX004.FAX04NTE
	  and FAX004EX.FAX01ESIS = FAX004.FAX04CDE
	  and FAX004EX.FAX04ELIN = FAX004.FAX04LIE

  if @Total > @PagadoNC begin
   insert FAT001 
                (FACDOC, FACTTR, CCM21COD, CLICOD, MONCOD,
                  TOTAL, SUBTOTAL, CG49CO, INDVOLPAG, FAM01COD, FAX01NTR, FAX01FCAM, FAX01FEC, CCTINDVV, PAGADONC, CODSISTEMA)   
         select top 1 FAX011.FACDOC, FAX011.TTRCOD, FAX001EX.CCM21COD, FAX001.CLICOD, FAX001.MONCOD,
                FAX001.FAX01TOT, @Total - @PagadoNC, FAX001.CG49CO, 'N', 
                 @FAM01COD, @FAX01NTR, FAX001.FAX01FCAM, FAX001.FAX01FEC, 'N', isnull(@PagadoNC, 0), FAX01ESIS
		FROM  FAX001, FAX011, FAX001EX, FAX004
		WHERE FAX001.FAM01COD = @FAM01COD
	          and FAX001.FAX01NTR = @FAX01NTR
	          and FAX001.FAM01COD = FAX011.FAM01COD
	          and FAX001.FAX01NTR = FAX011.FAX01NTR
		  and FAX001.FAM01COD = FAX004.FAM01COD
	          and FAX001.FAX01NTR = FAX004.FAX01NTR
        	  and FAX001EX.CCM21COD != null 
			  and FAX001EX.CCM21COD != 'ND'
		  and FAX001EX.FAX01ENUMDOC = FAX004.FAX04NTE
		  and FAX001EX.FAX01ESIS = FAX004.FAX04CDE
   		  and FAX011.FAX11LIN = (select max(FAX11LIN) from FAX011 b where b.FAM01COD = @FAM01COD and b.FAX01NTR = @FAX01NTR)
   group by   FAX011.FACDOC, FAX011.TTRCOD, FAX001EX.CCM21COD, FAX001.CLICOD, FAX001.MONCOD,
                FAX001.FAX01TOT,  FAX001.CG49CO, FAX001.FAX01FCAM, FAX001.FAX01FEC, FAX01ESIS
    insert FAT001D (FACDOC, FACTTR, CCM21COD, CTRO_COSTO, PRODUCTO, MONTO, CODSISTEMA, EMPRESA)
         select  FAX011.FACDOC, FAX011.TTRCOD, FAX001EX.CCM21COD, FAX001EX.CTRO_COSTO, FAX004EX.FAX04ECODPRO, 
		sum(isnull(FAX04TOT, 0)  - ( isnull(FAX04IMP, 0)+ isnull(FAX04ETIMBRE, 0))), FAX001EX.FAX01ESIS, FAX001EX.FAM33COD
	   FROM  FAX011, FAX001EX, FAX004, FAX004EX
		WHERE FAX011.FAM01COD = @FAM01COD
	          and FAX011.FAX01NTR = @FAX01NTR
		  and FAX004.FAM01COD = @FAM01COD
	          and FAX004.FAX01NTR = @FAX01NTR
        	  and FAX001EX.CCM21COD != null
			  and FAX001EX.CCM21COD != 'ND'
		  and FAX001EX.FAX01ENUMDOC = FAX004.FAX04NTE
		  and FAX001EX.FAX01ESIS = FAX004.FAX04CDE
		  and FAX004EX.FAX01ENUMDOC = FAX004.FAX04NTE
		  and FAX004EX.FAX01ESIS = FAX004.FAX04CDE
		  and FAX004EX.FAX04ELIN = FAX004.FAX04LIE
   		  and FAX011.FAX11LIN = (select max(FAX11LIN) from FAX011 b where b.FAM01COD = @FAM01COD and b.FAX01NTR = @FAX01NTR)
   group by   FAX011.FACDOC, FAX011.TTRCOD, FAX001EX.CCM21COD, FAX001EX.CTRO_COSTO, FAX004EX.FAX04ECODPRO, FAX001EX.FAX01ESIS, FAX001EX.FAM33COD
end



go
